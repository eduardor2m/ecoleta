import { useEffect, useState } from 'react';

import type { NextPage } from 'next';
import Head from 'next/head';
import { useRouter } from 'next/router';

import { CardPoint } from '../../components/CardPoint';
import { NavBar } from '../../components/NavBar';
import { usePoint } from '../../hooks/usePoint';
import styles from '../../styles/pages/List.module.scss';
import { Entity } from '../../types/entity';

const List: NextPage = () => {
  const router = useRouter();
  const [collectionPoints, setCollectionPoints] = useState<Entity[]>([]);

  const { listPointsByState } = usePoint();

  useEffect(() => {
    const state = router.query.state as string;
    setCollectionPoints(listPointsByState(state));
  }, [router.query.state]);

  return (
    <div className={styles.container}>
      <Head>
        <title>Ecoleta</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/assets/icon.svg" />
      </Head>

      <NavBar
        data={{
          icon: 'arrowLeft',
          description: 'Voltar para home',
          href: '/',
        }}
      />

      <main className={styles.main}>
        <section className={styles.wrapperQuantityPoints}>
          <div className={styles.quantityPoints}>
            <span>{collectionPoints.length} Pontos Encontrados</span>
          </div>
        </section>

        <section className={styles.content}>
          {collectionPoints.map((point) => (
            <CardPoint
              key={point.id}
              category={point.category}
              name={point.name}
              description={
                point.adress.city +
                ', ' +
                point.adress.state +
                ', ' +
                point.adress.street +
                ', NÂ° ' +
                point.adress.number
              }
              imageURL={point.image}
            />
          ))}
        </section>
      </main>
    </div>
  );
};

export default List;
