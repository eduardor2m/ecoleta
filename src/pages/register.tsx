import { useState } from 'react';
import { AiOutlineArrowLeft } from 'react-icons/ai';

import type { NextPage } from 'next';
import Head from 'next/head';
import Image from 'next/image';
import Link from 'next/link';

import { Success } from '../components/Success';
import styles from '../styles/pages/Register.module.scss';
import { Data } from './api/points';

const Register: NextPage = () => {
  const [open, setOpen] = useState(false);
  const [point, setPoint] = useState<Data>({} as Data);

  function handleAddPoint(data: Data) {
    const fetchData = async () => {
      await fetch('http://localhost:3000/api/point/add', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({
          id: data.title.length.toString() + new Date().getTime().toString(),
          title: data.title,
          description: 'data.description',
          adress: data.adress,
          image: '/assets/imageTalk.svg',
        }),
      });
    };

    fetchData();
  }

  return (
    <div className={styles.container}>
      <Head>
        <title>Ecoleta</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/assets/icon.svg" />
      </Head>

      <nav className={styles.navbar}>
        <Link href="/">
          <a href="/">
            <Image
              src="/assets/logo.svg"
              alt="Ecoleta"
              width={150}
              height={50}
            />
          </a>
        </Link>

        <Link href="/">
          <a href="/" className={styles.link}>
            <AiOutlineArrowLeft color="#34CB79" size={24} />
            <span className={styles.textLink}>Voltar para home</span>
          </a>
        </Link>
      </nav>

      <main className={styles.main}>
        <form className={styles.form}>
          <h1 className={styles.title}>
            Cadastro do <br /> ponto de coleta
          </h1>
          <h3 className={styles.subTitle}>Dados da entidade</h3>
          <label htmlFor="name" className={styles.label}>
            Nome da entidade
          </label>
          <input
            type="text"
            id="name"
            className={styles.inputName}
            onChange={(e) => setPoint({ ...point, title: e.target.value })}
          />
          <section className={styles.adressOne}>
            <section className={styles.wrapper}>
              <label htmlFor="adressName" className={styles.label}>
                Endereço
              </label>
              <input
                type="text"
                id="adressName"
                className={styles.inputAdressName}
                onChange={(e) =>
                  setPoint({
                    ...point,
                    adress: { ...point.adress, street: e.target.value },
                  })
                }
              />
            </section>
            <section className={styles.wrapper}>
              <label
                htmlFor="adressNumber"
                className={styles.label}
                style={{
                  marginLeft: '50%',
                }}
              >
                Número
              </label>
              <input
                type="number"
                id="adressNumber"
                className={styles.inputAdressNumber}
                onChange={(e) =>
                  setPoint({
                    ...point,
                    adress: { ...point.adress, number: e.target.value },
                  })
                }
              />
            </section>
          </section>
          <section className={styles.adressTwo}>
            <section className={styles.wrapper}>
              <label htmlFor="adressCity" className={styles.label}>
                Cidade
              </label>
              <input
                type="text"
                id="adressCity"
                className={styles.inputAdressCity}
                onChange={(e) =>
                  setPoint({
                    ...point,
                    adress: { ...point.adress, city: e.target.value },
                  })
                }
              />
            </section>
            <section className={styles.wrapper}>
              <label
                htmlFor="adressState"
                className={styles.label}
                style={{
                  marginLeft: '5%',
                }}
              >
                Estado
              </label>
              <input
                type="text"
                id="adressState"
                className={styles.inputAdressState}
                onChange={(e) =>
                  setPoint({
                    ...point,
                    adress: { ...point.adress, state: e.target.value },
                  })
                }
              />
            </section>
          </section>
          <section className={styles.titleItens}>
            <h3>Ítens de coleta</h3>
            <span>Selecione um ou mais ítens abaixo</span>
          </section>

          <section className={styles.items}>
            <div
              className={styles.item}
              onClick={() =>
                setPoint({
                  ...point,
                  category: 'lampadas',
                })
              }
              style={{
                backgroundColor:
                  point.category === 'lampadas' ? '#34CB79' : '#fff',
              }}
            >
              <Image
                src="/assets/lamps.svg"
                alt="Lâmpadas"
                width={60}
                height={60}
              />
              <span className={styles.textSpan}>Lâmpadas</span>
            </div>
            <div
              className={styles.item}
              onClick={() =>
                setPoint({
                  ...point,
                  category: 'pilhas-e-baterias',
                })
              }
              style={{
                backgroundColor:
                  point.category === 'pilhas-e-baterias' ? '#34CB79' : '#fff',
              }}
            >
              <Image
                src="/assets/batteries.svg"
                alt="Pilhas e Baterias"
                width={60}
                height={60}
              />
              <span className={styles.textSpan}>Pilhas e Baterias</span>
            </div>
            <div
              className={styles.item}
              onClick={() =>
                setPoint({
                  ...point,
                  category: 'papeis-e-papelao',
                })
              }
              style={{
                backgroundColor:
                  point.category === 'papeis-e-papelao' ? '#34CB79' : '#fff',
              }}
            >
              <Image
                src="/assets/papers.svg"
                alt="Papéis e Papelão"
                width={60}
                height={60}
              />
              <span className={styles.textSpan}>Papéis e Papelão</span>
            </div>
            <div
              className={styles.item}
              onClick={() =>
                setPoint({
                  ...point,
                  category: 'eletronicos',
                })
              }
              style={{
                backgroundColor:
                  point.category === 'eletronicos' ? '#34CB79' : '#fff',
              }}
            >
              <Image
                src="/assets/electronics.svg"
                alt="Resíduos Eletrônicos"
                width={60}
                height={60}
              />
              <span className={styles.textSpan}>Resíduos Eletrônicos</span>
            </div>
            <div
              className={styles.item}
              onClick={() =>
                setPoint({
                  ...point,
                  category: 'organicos',
                })
              }
              style={{
                backgroundColor:
                  point.category === 'organicos' ? '#34CB79' : '#fff',
              }}
            >
              <Image
                src="/assets/organic.svg"
                alt="Resíduos Orgânicos"
                width={60}
                height={60}
              />
              <span className={styles.textSpan}>Resíduos Orgânicos</span>
            </div>
            <div
              className={styles.item}
              onClick={() =>
                setPoint({
                  ...point,
                  category: 'oleo-de-cozinha',
                })
              }
              style={{
                backgroundColor:
                  point.category === 'oleo-de-cozinha' ? '#34CB79' : '#fff',
              }}
            >
              <Image
                src="/assets/oil.svg"
                alt="Óleo de Cozinha"
                width={60}
                height={60}
              />
              <span className={styles.textSpan}>Óleo de Cozinha</span>
            </div>
          </section>
          <section className={styles.wrapperButton}>
            <button
              type="submit"
              className={styles.button}
              onClick={(e) => {
                setOpen(true);
                // console.log(point);
                handleAddPoint(point);
                e.preventDefault();
              }}
            >
              Cadastrar ponto de coleta
            </button>
            {open && (
              <Success
                handleClick={() => {
                  setOpen(false);
                }}
              />
            )}
          </section>
        </form>
      </main>
    </div>
  );
};

export default Register;
